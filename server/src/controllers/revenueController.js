// Get revenue statistics
export const getRevenueStats = async (req, res) => {
    try {
        const { startDate, endDate } = req.query;

        const query = {
            gymId: req.gymId,
            isDeleted: false,
            isReversed: false // Exclude reversed revenues
        };

        if (startDate || endDate) {
            query.revenueDate = {};
            if (startDate) query.revenueDate.$gte = new Date(startDate);
            if (endDate) query.revenueDate.$lte = new Date(endDate);
        }

        // Total revenue (collected)
        const totalRevenue = await Revenue.aggregate([
            { $match: query },
            { $group: { _id: null, total: { $sum: '$amount' }, count: { $sum: 1 } } }
        ]);

        const collected = totalRevenue.length > 0 ? totalRevenue[0].total : 0;
        const count = totalRevenue.length > 0 ? totalRevenue[0].count : 0;

        // Revenue by source (with source details)
        const revenueBySource = await Revenue.aggregate([
            { $match: query },
            {
                $group: {
                    _id: '$sourceId',
                    total: { $sum: '$amount' },
                    count: { $sum: 1 }
                }
            },
            {
                $lookup: {
                    from: 'revenuesources',
                    localField: '_id',
                    foreignField: '_id',
                    as: 'source'
                }
            },
            { $unwind: '$source' },
            {
                $project: {
                    sourceId: '$_id',
                    sourceName: '$source.name',
                    category: '$source.category',
                    icon: '$source.icon',
                    color: '$source.color',
                    total: 1,
                    count: 1
                }
            },
            { $sort: { total: -1 } }
        ]);

        // Revenue by category (recurring vs one-time)
        const revenueByCategory = await Revenue.aggregate([
            { $match: query },
            {
                $lookup: {
                    from: 'revenuesources',
                    localField: 'sourceId',
                    foreignField: '_id',
                    as: 'source'
                }
            },
            { $unwind: '$source' },
            {
                $group: {
                    _id: '$source.category',
                    total: { $sum: '$amount' },
                    count: { $sum: 1 }
                }
            },
            { $sort: { total: -1 } }
        ]);

        // Revenue by generatedBy (system vs manual)
        const revenueByType = await Revenue.aggregate([
            { $match: query },
            {
                $group: {
                    _id: '$generatedBy',
                    total: { $sum: '$amount' },
                    count: { $sum: 1 }
                }
            }
        ]);

        // Calculate expected revenue
        const { calculateCollectionEfficiency } = await import('../services/autoRevenueService.js');
        let efficiency = null;

        if (startDate && endDate) {
            try {
                efficiency = await calculateCollectionEfficiency(
                    req.gymId,
                    startDate,
                    endDate
                );
            } catch (error) {
                console.error('Error calculating efficiency:', error);
            }
        }

        res.json({
            success: true,
            data: {
                total: collected,
                count: count,
                bySource: revenueBySource,
                byCategory: revenueByCategory,
                byType: revenueByType,
                // Expected revenue metrics (if date range provided)
                ...(efficiency && {
                    expected: efficiency.expected,
                    collected: efficiency.collected,
                    pending: efficiency.pending,
                    collectionEfficiency: efficiency.efficiency
                })
            }
        });
    } catch (error) {
        console.error('Error fetching revenue stats:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to fetch revenue statistics',
            error: error.message
        });
    }
};

