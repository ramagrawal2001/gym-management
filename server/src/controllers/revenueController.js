import Revenue from '../models/Revenue.js';

// Get all revenues with filters
export const getRevenues = async (req, res) => {
    try {
        const {
            page = 1,
            limit = 20,
            startDate,
            endDate,
            sourceId, // NEW: Filter by sourceId instead of source
            generatedBy, // NEW: Filter by system/manual
            category, // NEW: Filter by recurring/one-time
            search
        } = req.query;

        const query = {
            gymId: req.gymId,
            isDeleted: false
        };

        // Date range filter
        if (startDate || endDate) {
            query.revenueDate = {};
            if (startDate) query.revenueDate.$gte = new Date(startDate);
            if (endDate) query.revenueDate.$lte = new Date(endDate);
        }

        // Source filter (by sourceId)
        if (sourceId) {
            query.sourceId = sourceId;
        }

        // Generated by filter (system/manual)
        if (generatedBy) {
            query.generatedBy = generatedBy;
        }

        // Search filter
        if (search) {
            query.$or = [
                { description: { $regex: search, $options: 'i' } },
                { notes: { $regex: search, $options: 'i' } }
            ];
        }

        const skip = (page - 1) * limit;

        let revenues = await Revenue.find(query)
            .populate('sourceId') // Populate revenue source details
            .populate('paymentId')
            .populate('memberId', 'name email')
            .populate('createdBy', 'name email')
            .sort({ revenueDate: -1 })
            .limit(parseInt(limit))
            .skip(skip);

        // Filter by category if provided (requires populated sourceId)
        if (category) {
            revenues = revenues.filter(rev => rev.sourceId?.category === category);
        }

        const total = await Revenue.countDocuments(query);

        res.json({
            success: true,
            data: revenues,
            pagination: {
                page: parseInt(page),
                limit: parseInt(limit),
                total,
                pages: Math.ceil(total / limit)
            }
        });
    } catch (error) {
        console.error('Error fetching revenues:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to fetch revenues',
            error: error.message
        });
    }
};

