// Create revenue
export const createRevenue = async (req, res) => {
    try {
        const {
            amount,
            sourceId, // NEW: Use sourceId instead of enum source
            description,
            revenueDate,
            notes,
            paymentId,
            memberId
        } = req.body;

        // Validate sourceId is provided
        if (!sourceId) {
            return res.status(400).json({
                success: false,
                message: 'Revenue source is required'
            });
        }

        // Fetch the revenue source to validate
        const { default: RevenueSource } = await import('../models/RevenueSource.js');
        const revenueSource = await RevenueSource.findOne({
            _id: sourceId,
            gymId: req.gymId,
            isActive: true,
            isDeleted: false
        });

        if (!revenueSource) {
            return res.status(404).json({
                success: false,
                message: 'Revenue source not found or inactive'
            });
        }

        // CRITICAL: Block manual creation for auto-generated sources
        if (revenueSource.autoGenerate) {
            return res.status(400).json({
                success: false,
                message: `Cannot manually create revenue for "${revenueSource.name}". This source is auto-generated from ${revenueSource.linkedModule} payments.`,
                hint: 'Use Payment or Invoice modules instead. Revenue will be created automatically.'
            });
        }

        // Create revenue (manual)
        const revenue = await Revenue.create({
            amount,
            sourceId,
            description,
            revenueDate: revenueDate || Date.now(),
            notes,
            paymentId,
            memberId,
            gymId: req.gymId,
            createdBy: req.user._id,
            generatedBy: 'manual', // Manually created
            referenceType: paymentId ? 'payment' : null,
            referenceId: paymentId || null
        });

        const populatedRevenue = await Revenue.findById(revenue._id)
            .populate('sourceId')
            .populate('paymentId')
            .populate('memberId', 'name email')
            .populate('createdBy', 'name email');

        res.status(201).json({
            success: true,
            message: 'Revenue created successfully',
            data: populatedRevenue
        });
    } catch (error) {
        console.error('Error creating revenue:', error);
        res.status(500).json({
            success: false,
            message: 'Failed to create revenue',
            error: error.message
        });
    }
};

// Update revenue
